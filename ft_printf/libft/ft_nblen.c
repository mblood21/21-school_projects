#include "../ft_printf.h"

int		ft_nblen(int nb)
{
	int			i;

	i = 0;
	if (nb == -2147483648)
		return (10);
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
